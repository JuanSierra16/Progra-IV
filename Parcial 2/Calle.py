class Calle:

    #Constructor
    def __init__(self, numero):
        self._numero = numero
        self._atleta
        self._tiempo
    
    #Metodos
    def getNumero(self):
        return self._numero
    
    def getAtleta(self):
        return self._atleta

    def getTiempo(self):
        return self._tiempo

    def setAtleta(self,atleta):
        self._atleta = atleta
    
    def setTiempo(self,tiempo):
        self._tiempo = tiempo